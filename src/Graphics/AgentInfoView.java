    private void _loopProperties()
    {
        if (this._target instanceof SolverAgent)
        {
            SolverAgent _mask = (SolverAgent)this._target;
            this._addText("Skills: ");
            for (cSkill i: _mask.getSkills())
                if (i.getName().equals(FactoryHolder._configManager.getArrayValue("AGENT_SKILLS").get(FactoryHolder._configManager.getArrayValue("AGENT_SKILLS").size() - 1)))
                    this._addText(i.getName());
                else
                    this._addText(i.getName() + ", ");
            this._addText("\n");
            
            this._addText("Experience: " + _mask.getTotalExperience() + "\n");
            this._addText("Reward: " + _mask.getStats()._money + "\n");
            this._addText("Rejected Challenges: " + _mask.getStats()._rejected + "\n");
            this._addText("Solved Challenges: " + _mask.getStats()._successTrials + "\n");
            this._addText("Challenge Trials: " + _mask.getStats()._trials + "\n");
            this._addText("Challenge Fails: " + (_mask.getStats()._trials - _mask.getStats()._successTrials) + "\n");
            this._addText("\n");
            
            //this._addText("Last Challenges:\n");
            
        }
        // TODO: Rest of agents
    }
    
    public void setUpContent(Object _agent)
    {
        if (_agent instanceof SolverAgent)
        {
            this.jTextField1.setText(_agent.toString());
            this.jTextField2.setText("Solver Agent");
            this.jTextField3.setText(_agent.toString().split("#")[1]);
            this._target = _agent;
            this.jTextPane1.setText("");
            this._loopProperties();
        } else if (_agent instanceof ProposerAgent) {
            this.jTextField1.setText(_agent.toString());
            this.jTextField2.setText("Proposer Agent");
            this.jTextField3.setText(_agent.toString().split("#")[1]);
            this._target = _agent;
            this.jTextPane1.setText("");
            this._loopProperties();
        } else if (_agent instanceof Challenge) {
            this.jTextField1.setText(_agent.toString());
            this.jTextField2.setText("Challenge Entity");
            this.jTextField3.setText(_agent.toString().split("#")[1]);
            this._target = _agent;
            this.jTextPane1.setText("");
            this._loopProperties();
        } else {
            FactoryHolder._logManager.print(ILogManager._LOG_TYPE.TYPE_ERROR, "AgentInfoView request instance not recognized: " + _agent.toString());
        }
    }
    
